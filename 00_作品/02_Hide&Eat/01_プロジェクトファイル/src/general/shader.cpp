#include "shader.h"

//=============================================================
// [Shader] 描画開始
//=============================================================
HRESULT Shader::Begin()
{
	return m_effect->Begin(&m_numPass, 0);
}

//=============================================================
// [Shader] 描画終了
//=============================================================
HRESULT Shader::End()
{
	return m_effect->End();
}

//=============================================================
// [Shader] パス開始
//=============================================================
HRESULT Shader::BeginPass(const UINT& pass)
{
	return m_effect->BeginPass(pass);
}

//=============================================================
// [Shader] パス終了
//=============================================================
HRESULT Shader::EndPass()
{
	return m_effect->EndPass();
}