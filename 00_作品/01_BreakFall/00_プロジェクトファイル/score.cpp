#include "score.h"
#include "number.h"

//========================================================================================================
//初期化処理
//========================================================================================================
void Score::Init()
{
	//スコアを設定する
	this->GetComponent<Number>()->SetNumber(this->m_nScore);
}

//========================================================================================================
//更新処理
//========================================================================================================
void Score::Update()
{
}

//========================================================================================================
//加算処理
//========================================================================================================
void Score::Add(int nValue)
{
	//加算する
	this->m_nScore += nValue;
	this->GetComponent<Number>()->SetNumber(this->m_nScore);
}