#include "pause.h"
#include "prefab.h"
#include "pause_menu.h"

//========================================================================================================
//初期化処理
//========================================================================================================
void Pause::Init()
{
	//ポーズメニューを生成する
	this->m_pPauseMenu = PauseMenuPrefab::Create();
}

//========================================================================================================
//終了処理
//========================================================================================================
void Pause::Uninit()
{
}

//========================================================================================================
//更新処理
//========================================================================================================
void Pause::Update()
{
	//ポーズメニューの更新処理を行う
	this->m_pPauseMenu->Update();
}

//========================================================================================================
//描画処理
//========================================================================================================
void Pause::Draw()
{
}

//========================================================================================================
//ポーズの切り替え処理
//========================================================================================================
void Pause::SwitchPause()
{
	//ポーズを切り替える
	this->m_isPause = this->m_isPause ? false : true;
	if (this->m_isPause == true)
	{//ポーズ中に切り替わった場合
		//ポーズメニューを初期化する
		this->m_pPauseMenu->Init();
		this->m_pPauseMenu->SetActive(true);   //アクティブ化
	}
	else
	{
		//非アクティブ化する
		this->m_pPauseMenu->SetActive(false);
	}
}