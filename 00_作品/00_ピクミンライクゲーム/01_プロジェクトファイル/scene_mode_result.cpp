#include "scene_mode.h"
#include "scene.h"
#include "prefab.h"

//========================================================================================================
//初期化処理
//========================================================================================================
void SceneModeResult::Init()
{
	//背景を生成する
	Background2DPrefab::Create(Manager::Get()->GetTexture()->Get("result_00.png"));

	//フェードを生成する
	FadePrefab::Create();
}

//========================================================================================================
//終了処理
//========================================================================================================
void SceneModeResult::Uninit()
{
	//オブジェクトを一括破棄する
	Object::DestroyAll();
}

//========================================================================================================
//更新処理
//========================================================================================================
void SceneModeResult::Update()
{
	Keyboard* pKeyboard = Manager::Get()->GetKeyboard();   //キーボード

	//オブジェクトを一括更新する
	Object::UpdateAll();

	if (pKeyboard->GetTrigger(DIK_RETURN))
	{//Enterキーが押された場合
		//終了フラグを立てる
		this->GetScene()->Next(Scene::MODE::IS_TITLE);
	}
}

//========================================================================================================
//描画処理
//========================================================================================================
void SceneModeResult::Draw()
{
	//オブジェクトを一括描画する
	Object::DrawAll();
}