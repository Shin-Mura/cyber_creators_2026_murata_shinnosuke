#include "insect.h"
#include "cargo.h"

//静的メンバ変数を初期化する
const float Insect::MOVE_SPEED = 1.5f;   //移動量
const float Insect::MOVE_THROW = 22.5f;   //投げられた時の移動量

//========================================================================================================
//初期化処理
//========================================================================================================
void Insect::Init()
{
}

//========================================================================================================
//更新処理
//========================================================================================================
void Insect::Update()
{
	//状態のハンドルを行う
	this->m_pState->Handle();
}

//========================================================================================================
//状態の変更
//========================================================================================================
void Insect::ChangeState(InsectState* state)
{
	//現在の状態を確認する
	if (this->m_pState == nullptr)
	{
		//状態を設定する
		this->m_pState = state;
	}
	else
	{
		//状態を上書きする
		delete this->m_pState;
		this->m_pState = state;
	}
}