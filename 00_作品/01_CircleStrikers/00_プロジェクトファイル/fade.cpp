#include "fade.h"
#include "rendering.h"

//========================================================================================================
//初期化処理
//========================================================================================================
void Fade::Init()
{
}

//========================================================================================================
//更新処理
//========================================================================================================
void Fade::Update()
{
	//透明度を下げる
	D3DXCOLOR col = this->GetObj()->GetComponent<Rendering>()->GetColor();
	col.a -= 0.1f;
	if (col.a <= 0.0f)
	{//最小値に達した場合
		//死亡フラグを立てる
		this->GetObj()->Death();

		//処理を終了する
		return;
	}

	//透明度を設定する
	this->GetObj()->GetComponent<Rendering>()->SetColor(col);
}