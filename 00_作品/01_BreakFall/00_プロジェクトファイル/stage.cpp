#include "stage.h"
#include "prefab.h"
#include "waypoint.h"

//=======================================================================================================
//読み込み
//=======================================================================================================
void Stage::Load(Stage::TYPE type)
{
	//種類によって処理を変える
	switch (type)
	{
	case Stage::TYPE::IS_MAIN:
		//デモステージを読み込む
		this->LoadMain();

		break;
	}
}

//=======================================================================================================
//読み込み
//=======================================================================================================
void Stage::LoadMain()
{
	//棒を生成する
	StickPrefab::Create();

	//ブロックスポナーを生成する
	BlockSpawnerPrefab::Create();

	//ボールを生成する
	BallPrefab::Create();
}