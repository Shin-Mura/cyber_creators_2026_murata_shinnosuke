#include "input_action.h"

//静的メンバ変数を初期化する
InputAction* InputAction::m_apInputAction[InputAction::MAX_DATA] = {};

//===========================================================================================================
//コンストラクタ
//===========================================================================================================
InputAction::InputAction()
{
	//メンバ変数を初期化する
	this->m_nId = -1;   //番号
}

//===========================================================================================================
//デストラクタ
//===========================================================================================================
InputAction::~InputAction()
{
}

//===========================================================================================================
//初期化処理
//===========================================================================================================
void InputAction::Init()
{
}

//===========================================================================================================
//終了処理
//===========================================================================================================
void InputAction::Uninit()
{
}

//===========================================================================================================
//終了処理
//===========================================================================================================
void InputAction::Release()
{
	//自分の番号を保存する
	int nId = this->m_nId;

	//自分を解放する
	InputAction::m_apInputAction[nId]->Uninit();
	delete InputAction::m_apInputAction[nId];
	InputAction::m_apInputAction[nId] = nullptr;
}